  `lens_sn` varchar(32) DEFAULT NULL COMMENT '镜头批号',
  `fpga_version` varchar(32) DEFAULT NULL COMMENT 'FPGA版本号',
  `order_state` int DEFAULT '0' COMMENT '工单状态',
  `completed_count` int DEFAULT '0',
  `product_type` varchar(16) DEFAULT NULL,
  PRIMARY KEY (`id`),
  UNIQUE KEY `aq_` (`work_order_code`,`order_date`,`material_code`,`produce_count`)
) ENGINE=InnoDB AUTO_INCREMENT=363 DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci COMMENT='生产工单信息表';