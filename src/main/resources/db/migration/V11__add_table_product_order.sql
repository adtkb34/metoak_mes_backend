CREATE TABLE IF NOT EXISTS  `mo_produce_order` (
  `id` int NOT NULL AUTO_INCREMENT,
  `work_order_code` varchar(32) CHARACTER SET utf8mb4 COLLATE utf8mb4_0900_ai_ci DEFAULT NULL COMMENT '工单编号',
  `order_date` date DEFAULT NULL COMMENT '单据日期',
  `material_code` varchar(16) DEFAULT NULL COMMENT '物料编号',
  `material_name` varchar(256) DEFAULT NULL COMMENT '物料名称',
  `model_type` varchar(64) DEFAULT NULL COMMENT '规格型号',
  `workshop` varchar(32) DEFAULT NULL COMMENT '生产车间',
  `produce_count` int DEFAULT NULL COMMENT '计划生产数量',
  `produce_unit` varchar(16) DEFAULT NULL COMMENT '产品单位',
